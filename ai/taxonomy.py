# Runtime-mutable state — updated when user loads custom taxonomy from Excel.
# Providers must reference this module's attributes (not cache them as locals)
# so they pick up changes without restart.
_ACTIVE_CATEGORIES: list[str] = CATEGORIES_UI
_ACTIVE_GUIDE: str = CATEGORY_GUIDE


def get_active() -> tuple[list[str], str]:
    """Return currently active taxonomy (custom or built-in)."""
    return _ACTIVE_CATEGORIES, _ACTIVE_GUIDE


def apply_custom(categories: list[str], guide: str) -> None:
    """Replace active taxonomy at runtime."""
    global _ACTIVE_CATEGORIES, _ACTIVE_GUIDE
    _ACTIVE_CATEGORIES = categories
    _ACTIVE_GUIDE = guide


def _load_startup_custom() -> None:
    try:
        from utils.taxonomy_excel import load_custom
        result = load_custom()
        if result:
            apply_custom(*result)
    except Exception:
        pass


_load_startup_custom()

